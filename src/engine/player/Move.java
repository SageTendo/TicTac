package engine.player;

/**
 * Representation of a move
 */
public class Move {

  public int row;
  public int col;

  /**
   * @return the row to be played
   */
  public int getRow() {
    return row;
  }

  /**
   * @return the column to be played
   */
  public int getCol() {
    return col;
  }
}
